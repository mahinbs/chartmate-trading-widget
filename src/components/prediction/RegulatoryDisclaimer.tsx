import { Alert, AlertDescription } from "@/components/ui/alert";
import { Card, CardContent } from "@/components/ui/card";
import { Checkbox } from "@/components/ui/checkbox";
import { AlertCircle, Shield, FileText } from "lucide-react";
import { useState } from "react";

interface RegulatoryDisclaimerProps {
  onAcknowledge?: (acknowledged: boolean) => void;
  requireAcknowledgment?: boolean;
}

export function RegulatoryDisclaimer({ 
  onAcknowledge, 
  requireAcknowledgment = false 
}: RegulatoryDisclaimerProps) {
  const [riskAcknowledged, setRiskAcknowledged] = useState(false);
  const [disclaimerAcknowledged, setDisclaimerAcknowledged] = useState(false);

  const handleAcknowledgment = (type: 'risk' | 'disclaimer', checked: boolean) => {
    if (type === 'risk') {
      setRiskAcknowledged(checked);
    } else {
      setDisclaimerAcknowledged(checked);
    }
    
    if (onAcknowledge) {
      const bothAcknowledged = type === 'risk' 
        ? checked && disclaimerAcknowledged 
        : riskAcknowledged && checked;
      onAcknowledge(bothAcknowledged);
    }
  };

  return (
    <Card className="border-orange-500/30 bg-orange-500/5">
      <CardContent className="pt-6 space-y-4">
        
        {/* Main Disclaimer */}
        <Alert className="border-orange-500/50">
          <AlertCircle className="h-4 w-4" />
          <AlertDescription>
            <p className="font-semibold mb-2">⚠️ Important Disclaimer</p>
            <p className="text-sm leading-relaxed">
              This prediction is generated by AI analysis and is for <strong>informational purposes only</strong>. 
              It does not constitute financial advice, investment recommendation, or a guarantee of future performance.
            </p>
          </AlertDescription>
        </Alert>

        {/* Risk Warnings Grid */}
        <div className="grid md:grid-cols-2 gap-3">
          <div className="p-3 bg-background rounded-lg border border-orange-500/20">
            <div className="flex items-start gap-2">
              <Shield className="h-4 w-4 text-orange-500 mt-0.5" />
              <div>
                <p className="font-semibold text-sm mb-1">Market Risk</p>
                <p className="text-xs text-muted-foreground">
                  Stock markets are subject to market risks. Past performance does not guarantee future results.
                </p>
              </div>
            </div>
          </div>

          <div className="p-3 bg-background rounded-lg border border-orange-500/20">
            <div className="flex items-start gap-2">
              <FileText className="h-4 w-4 text-orange-500 mt-0.5" />
              <div>
                <p className="font-semibold text-sm mb-1">No Guarantee</p>
                <p className="text-xs text-muted-foreground">
                  AI predictions can be wrong. You may lose part or all of your investment.
                </p>
              </div>
            </div>
          </div>

          <div className="p-3 bg-background rounded-lg border border-orange-500/20">
            <div className="flex items-start gap-2">
              <AlertCircle className="h-4 w-4 text-orange-500 mt-0.5" />
              <div>
                <p className="font-semibold text-sm mb-1">Leverage Warning</p>
                <p className="text-xs text-muted-foreground">
                  Leverage trading amplifies both gains and losses. Use with extreme caution.
                </p>
              </div>
            </div>
          </div>

          <div className="p-3 bg-background rounded-lg border border-orange-500/20">
            <div className="flex items-start gap-2">
              <Shield className="h-4 w-4 text-orange-500 mt-0.5" />
              <div>
                <p className="font-semibold text-sm mb-1">Consult Professional</p>
                <p className="text-xs text-muted-foreground">
                  Always consult a qualified financial advisor before making investment decisions.
                </p>
              </div>
            </div>
          </div>
        </div>

        {/* Legal Statements */}
        <div className="p-4 bg-muted/50 rounded-lg border text-xs space-y-2">
          <p>
            <strong>Past Performance:</strong> Historical data and AI predictions do not guarantee future results. 
            Market conditions change constantly.
          </p>
          <p>
            <strong>Accuracy:</strong> While our AI uses advanced analysis, predictions may be inaccurate. 
            Always do your own research.
          </p>
          <p>
            <strong>Liability:</strong> We are not responsible for any losses incurred from using these predictions. 
            Trade at your own risk.
          </p>
          <p>
            <strong>Regulation:</strong> Ensure your trading activities comply with local laws and regulations.
          </p>
        </div>

        {/* Acknowledgment Checkboxes (if required) */}
        {requireAcknowledgment && (
          <div className="space-y-3 pt-2 border-t">
            <div className="flex items-start gap-3">
              <Checkbox 
                id="risk-ack"
                checked={riskAcknowledged}
                onCheckedChange={(checked) => handleAcknowledgment('risk', checked as boolean)}
              />
              <label 
                htmlFor="risk-ack" 
                className="text-sm cursor-pointer leading-tight"
              >
                I understand that trading involves risk and I may lose my entire investment
              </label>
            </div>

            <div className="flex items-start gap-3">
              <Checkbox 
                id="disclaimer-ack"
                checked={disclaimerAcknowledged}
                onCheckedChange={(checked) => handleAcknowledgment('disclaimer', checked as boolean)}
              />
              <label 
                htmlFor="disclaimer-ack" 
                className="text-sm cursor-pointer leading-tight"
              >
                I acknowledge this is AI-generated prediction and not guaranteed financial advice
              </label>
            </div>
          </div>
        )}

        {/* Footer */}
        <p className="text-center text-xs text-muted-foreground pt-2 border-t">
          By using this service, you agree to our Terms of Service and acknowledge all risk warnings
        </p>

      </CardContent>
    </Card>
  );
}
